"What is the meaning of Art? What purpose does Art serve? Why does Art... Art?"
There are many questions and answers as to why art exists and its significance with in society. Art is one of the greatest ways of expressing just about anything. Emotions, stories, history, and probably 997 other words but to put it simply. Art is the evidence of human existance.  
Art is complex in its own nature because what some people see as a nice formation of shapes and colors other see a story. A story with varying complexities that detail every line drawn, brush stroke, and meme created. The purpose of this Database Web Interface is
to provide a look into Mural Street Art around the greater Richmond Area.  

There are a lot mural located in Richmond VA which are not documented such as what is the name of the mural, 
who painted it, what techniques were used, and where is the art located? We created this Mural Database which contains data for Murals, Artists, Locations of Mural, Images of murals, awards etc on greater Richmond area where 
everyone can find most of the information regarding the mural they want to see which might have not been documented or became a victim of time... 

